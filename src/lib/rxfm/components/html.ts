import { componentCreator, ComponentCreator } from "./component";

function getHTMLComponentCreator<K extends keyof HTMLElementTagNameMap>(
  tagName: K,
): ComponentCreator<HTMLElementTagNameMap[K]> {
  return componentCreator(() => document.createElement(tagName));
}

